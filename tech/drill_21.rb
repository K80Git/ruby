問題

二桁以上の整数を入力すると、十の位と一の位の数字をもとに、足し算、掛け算を行い、最後にそれぞれの結果を足し合わせて出力するプログラムをRubyで作成してください。

ターミナルでの入力例

> 二桁の整数を入力してください
> 15

# 1 + 5 と 1 × 5 をそれぞれ計算

> 足し算結果と掛け算結果の合計値は11です


=end
